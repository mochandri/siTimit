package com.example.sitimit;

public class ModelGalvalum {
    int id;
    String nama_galvalum, harga_galvalum;

    public ModelGalvalum() {
        this.id = id;
        this.nama_galvalum = nama_galvalum;
        this.harga_galvalum = harga_galvalum;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama_galvalum() {
        return nama_galvalum;
    }

    public void setNama_galvalum(String nama_galvalum) {
        this.nama_galvalum = nama_galvalum;
    }

    public String getHarga_galvalum() {
        return harga_galvalum;
    }

    public void setHarga_galvalum(String harga_galvalum) {
        this.harga_galvalum = harga_galvalum;
    }
}
