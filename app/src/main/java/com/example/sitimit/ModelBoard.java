package com.example.sitimit;

public class ModelBoard {
    int id;
    String nama_board, harga_board;

    public ModelBoard() {
        this.id = id;
        this.nama_board = nama_board;
        this.harga_board = harga_board;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama_board() {
        return nama_board;
    }

    public void setNama_board(String nama_board) {
        this.nama_board = nama_board;
    }

    public String getHarga_board() {
        return harga_board;
    }

    public void setHarga_board(String harga_board) {
        this.harga_board = harga_board;
    }
}
